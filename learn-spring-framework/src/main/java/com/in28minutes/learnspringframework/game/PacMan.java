package com.in28minutes.learnspringframework.game;

public class PacMan implements GameConsole{
	
	public void up()
	{
		System.out.println("UP");
	}
	public void down()
	{
		System.out.println("Sit down");
	}
	public void left()
	{
		System.out.println("Go Back");
	}
	public void right()
	{
		System.out.println("Move ahead");
	}

}
